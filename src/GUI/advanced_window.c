static void update_gadget_states(struct iTidyAdvancedWindow *adv_data)
{
    ULONG selected = 0;
    
    /* Check Auto Max Checkbox state */
    GetAttr(CHECKBOX_Checked, adv_data->gadgets[ITIDY_ADV_AUTO_MAX_CHECK], &selected);
    adv_data->auto_max_enabled = (BOOL)selected;
    
    /* Enable/Disable Max Icons integer gadget based on checkbox */
    SetGadgetAttrs((struct Gadget *)adv_data->gadgets[ITIDY_ADV_MAX_ICONS_ROW], adv_data->window, NULL,
        GA_Disabled, adv_data->auto_max_enabled,
        TAG_END);
}

static const int aspect_ratio_presets[] = { 750, 1000, 1300, 1600, 2000, 2400 };

/* Safe getter for Integer gadgets (handles NULL) */
static ULONG get_integer_val(Object *obj)
{
    ULONG val = 0;
    GetAttr(INTEGER_Number, obj, &val);
    return val; 
}

/* Safe getter for Chooser/Checkbox gadgets (handles NULL) */
static ULONG get_choice_val(Object *obj)
{
    ULONG val = 0;
    GetAttr(CHOOSER_Selected, obj, &val); /* Works for GA_Selected too usually, but let's be safe */
    /* Checkbox uses GA_Selected (CHECKBOX_Checked deprecated but alias) */
    if (val > 65535) val = 0; /* Sanity check for -1 */
    return val;
}

static ULONG get_checkbox_val(Object *obj)
{
    ULONG val = 0;
    GetAttr(GA_Selected, obj, &val);
    return val;
}

static void save_settings_from_gadgets(struct iTidyAdvancedWindow *adv_data)
{
    if (!adv_data || !adv_data->prefs) return;
    LayoutPreferences *prefs = adv_data->prefs;

    /* Aspect Ratio: Map Index -> Value */
    ULONG idx = get_choice_val(adv_data->gadgets[ITIDY_ADV_ASPECT_CHOOSER]);
    if (idx < 6) prefs->aspectRatio = aspect_ratio_presets[idx];
    else prefs->aspectRatio = 2000; /* Default Wide */

    /* Overflow Mode */
    idx = get_choice_val(adv_data->gadgets[ITIDY_ADV_OVERFLOW_CHOOSER]);
    prefs->overflowMode = (WindowOverflowMode)idx;

    /* Icon Spacing */
    prefs->iconSpacingX = (UWORD)get_integer_val(adv_data->gadgets[ITIDY_ADV_SPACING_X]);
    prefs->iconSpacingY = (UWORD)get_integer_val(adv_data->gadgets[ITIDY_ADV_SPACING_Y]);

    /* Icons Per Row */
    prefs->minIconsPerRow = (UWORD)get_integer_val(adv_data->gadgets[ITIDY_ADV_MIN_ICONS_ROW]);
    
    /* Auto Max Checkbox */
    BOOL auto_max = (BOOL)get_checkbox_val(adv_data->gadgets[ITIDY_ADV_AUTO_MAX_CHECK]);
    if (auto_max) {
        prefs->maxIconsPerRow = 0;
    } else {
        prefs->maxIconsPerRow = (UWORD)get_integer_val(adv_data->gadgets[ITIDY_ADV_MAX_ICONS_ROW]);
    }

    /* Max Window Width % */
    /* Map Index -> Value: 0=Auto(0), 1=30, 2=50, 3=70, 4=90, 5=100 */
    idx = get_choice_val(adv_data->gadgets[ITIDY_ADV_MAX_WIDTH_CHOOSER]);
    switch(idx) {
        case 0: prefs->maxWindowWidthPct = 0; break;
        case 1: prefs->maxWindowWidthPct = 30; break;
        case 2: prefs->maxWindowWidthPct = 50; break;
        case 3: prefs->maxWindowWidthPct = 70; break;
        case 4: prefs->maxWindowWidthPct = 90; break;
        case 5: prefs->maxWindowWidthPct = 100; break;
        default: prefs->maxWindowWidthPct = 0; break;
    }

    /* Vertical Align */
    prefs->textAlignment = (TextAlignment)get_choice_val(adv_data->gadgets[ITIDY_ADV_VERT_ALIGN_CHOOSER]);

    /* Checkboxes */
    prefs->reverseSort = (BOOL)get_checkbox_val(adv_data->gadgets[ITIDY_ADV_REVERSE_SORT_CHECK]);
    prefs->useColumnWidthOptimization = (BOOL)get_checkbox_val(adv_data->gadgets[ITIDY_ADV_OPTIMIZE_COLS_CHECK]);
    
    /* Column Layout (mapped to LayoutMode) */
    BOOL use_cols = (BOOL)get_checkbox_val(adv_data->gadgets[ITIDY_ADV_COLUMN_LAYOUT_CHECK]);
    prefs->layoutMode = use_cols ? LAYOUT_MODE_COLUMN : LAYOUT_MODE_ROW;

    prefs->skipHiddenFolders = (BOOL)get_checkbox_val(adv_data->gadgets[ITIDY_ADV_SKIP_HIDDEN_CHECK]);
    prefs->stripNewIconBorders = (BOOL)get_checkbox_val(adv_data->gadgets[ITIDY_ADV_STRIP_BORDERS_CHECK]);

    /* Block Gap Size */
    prefs->blockGapSize = (BlockGapSize)get_choice_val(adv_data->gadgets[ITIDY_ADV_BLOCK_GAP_CHOOSER]);
}


static BOOL open_reaction_classes(void)
{
    /* Open classes with specific versions if needed, or 0 for any */
    if (!WindowBase) WindowBase = OpenLibrary("window.class", 0);
    if (!LayoutBase) LayoutBase = OpenLibrary("gadgets/layout.gadget", 0);
    if (!ButtonBase) ButtonBase = OpenLibrary("gadgets/button.gadget", 0);
    if (!CheckBoxBase) CheckBoxBase = OpenLibrary("gadgets/checkbox.gadget", 0);
    if (!ChooserBase) ChooserBase = OpenLibrary("gadgets/chooser.gadget", 0);
    if (!IntegerBase) IntegerBase = OpenLibrary("gadgets/integer.gadget", 0);
    if (!LabelBase) LabelBase = OpenLibrary("images/label.image", 0);
    if (!ClickTabBase) ClickTabBase = OpenLibrary("gadgets/clicktab.gadget", 0);
    if (!SpaceBase) SpaceBase = OpenLibrary("gadgets/space.gadget", 0);

    return (WindowBase && LayoutBase && ButtonBase && 
            CheckBoxBase && ChooserBase && IntegerBase && LabelBase &&
            ClickTabBase && SpaceBase);
}

static void close_reaction_classes(void)
{
    if (SpaceBase) { CloseLibrary(SpaceBase); SpaceBase = NULL; }
    if (ClickTabBase) { CloseLibrary(ClickTabBase); ClickTabBase = NULL; }
    if (LabelBase) { CloseLibrary(LabelBase); LabelBase = NULL; }
    if (IntegerBase) { CloseLibrary(IntegerBase); IntegerBase = NULL; }
    if (ChooserBase) { CloseLibrary(ChooserBase); ChooserBase = NULL; }
    if (CheckBoxBase) { CloseLibrary(CheckBoxBase); CheckBoxBase = NULL; }
    if (ButtonBase) { CloseLibrary(ButtonBase); ButtonBase = NULL; }
    if (LayoutBase) { CloseLibrary(LayoutBase); LayoutBase = NULL; }
    if (WindowBase) { CloseLibrary(WindowBase); WindowBase = NULL; }
}

static struct List *make_chooser_list(STRPTR *labels)
{
    struct List *list = (struct List *)whd_malloc(sizeof(struct List));
    if (list)
    {
        NewList(list);
        while (*labels)
        {
            struct Node *node = AllocChooserNode(CNA_Text, *labels, TAG_END);
            if (node)
            {
                AddTail(list, node);
            }
            labels++;
        }
    }
    return list;
}

static void free_chooser_list(struct List *list)
{
    if (list)
    {
        struct Node *node, *next;
        node = list->lh_Head;
        while ((next = node->ln_Succ))
        {
            FreeChooserNode(node);
            node = next;
        }
        whd_free(list);
    }
}

static struct List *make_clicktab_list(STRPTR *labels)
{
    struct List *list = (struct List *)whd_malloc(sizeof(struct List));
    if (list)
    {
        WORD tab_number = 0;
        NewList(list);
        while (*labels)
        {
            struct Node *node = AllocClickTabNode(TNA_Text, *labels, TNA_Number, tab_number, TAG_END);
            if (node)
            {
                AddTail(list, node);
            }
            labels++;
            tab_number++;
        }
    }
    return list;
}

static void free_clicktab_list(struct List *list)
{
    if (list)
    {
        struct Node *node, *next;
        node = list->lh_Head;
        while ((next = node->ln_Succ))
        {
            FreeClickTabNode(node);
            node = next;
        }
        whd_free(list);
    }
}
