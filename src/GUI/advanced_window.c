    /* Map OverflowMode to Chooser Index */
    WORD overflow_idx = 0;
    if (prefs->overflowMode == OVERFLOW_HORIZONTAL) overflow_idx = 0;
    else if (prefs->overflowMode == OVERFLOW_VERTICAL) overflow_idx = 1;
    else overflow_idx = 2;

    /* Map Aspect Ratio Value to Preset Index */
    /* 750=0, 1000=1, 1300=2, 1600=3, 2000=4 */
