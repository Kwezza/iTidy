    if (ar <= 875) aspect_idx = 0;      // ~750
    else if (ar <= 1150) aspect_idx = 1; // ~1000
    else if (ar <= 1450) aspect_idx = 2; // ~1300
    else if (ar <= 1800) aspect_idx = 3; // ~1600
